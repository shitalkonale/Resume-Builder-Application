    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
           
         
         try{
             
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/resumeapp","root","");
            Statement st = conn.createStatement();
            
             String name=username.getText();
            String email= mail.getText();
            String pass=password.getText();
            String conpass=conpassword.getText();
             if(username.getText().length()==0)
            JOptionPane.showMessageDialog(null, "Please Enter Username");
           
           
           else if(mail.getText().length()==0)
            JOptionPane.showMessageDialog(null, "Please Enter Email");
           
           else if(password.getPassword().length==0)
            JOptionPane.showMessageDialog(null, "Please Enter Password");
        
           else if(conpassword.getPassword().length==0)
            JOptionPane.showMessageDialog(null, "Please Enter Conform Password");
           
           else if(pass.equals(conpass)){
               
                JOptionPane.showMessageDialog(this, "User Successfully Registered","Success",JOptionPane.INFORMATION_MESSAGE);                  
                st.execute("insert into login values('"+name+"','"+email+"','"+pass+"','"+conpass+"')");  
                 this.setVisible(false);
                new ResumeTamplate().setVisible(true);
           }else{
                JOptionPane.showMessageDialog(this, "Please Enter Same Confirm Password","User not Registered",JOptionPane.ERROR_MESSAGE);
           }
            
          
                    
         }
           
         catch(Exception e){
             JOptionPane.showMessageDialog(this, "record not inserted");
             
         }
           
           
       
    
        
    }//GEN-LAST:event_jButton1ActionPerformed

